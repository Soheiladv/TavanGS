def get_theme_choices():
    """دریافت لیست تم‌ها برای مدل"""
    return [(key, config['name']) for key, config in THEME_CONFIGS.items()]

def get_theme_config(theme_name):
    """دریافت تنظیمات یک تم خاص"""
    return THEME_CONFIGS.get(theme_name, THEME_CONFIGS['default'])

def get_all_themes():
    """دریافت تمام تم‌ها"""
    return THEME_CONFIGS

def get_core_themes():
    """دریافت تم‌های اصلی سیستم"""
    core_theme_keys = ['default', 'light', 'dark', 'blue', 'green']
    return {key: THEME_CONFIGS[key] for key in core_theme_keys if key in THEME_CONFIGS}

def get_extra_themes():
    """دریافت تم‌های اضافی"""
    core_theme_keys = ['default', 'light', 'dark', 'blue', 'green']
    return {key: THEME_CONFIGS[key] for key in THEME_CONFIGS if key not in core_theme_keys}

def get_core_theme_choices():
    """دریافت choices تم‌های اصلی"""
    core_themes = get_core_themes()
    return [(key, config['name']) for key, config in core_themes.items()]

def get_extra_theme_choices():
    """دریافت choices تم‌های اضافی"""
    extra_themes = get_extra_themes()
    return [(key, config['name']) for key, config in extra_themes.items()]
